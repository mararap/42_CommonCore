#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;

	i = 0;
	if (ac != 2)
		return (write(1, "\n", 1), 0);
	while(av[1][i])
	{
		if(av[1][i] >= 65 && av[1][i] <= 90)
			av[1][i] = (65 + (26 - (av[1][i] - 64)));
		else if (av[1][i] >= 97 && av[1][i] <= 122)
			av[1][i] = (97 + (26 - (av[1][i] - 96)));
		write(1, &av[1][i], 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
