#include <unistd.h>

int	main (int ac, char **av)
{
	int	i = 0;

	if (ac != 2)
		return (write(1, "\n", 1), -1);
	while (av[1][i])
	{
		if (av[1][i] <= 32)
			i++;
		else
		{
			write (1, &av[1][i], 1);
			i++;
			if((av[1][i] == ' ' || av[1][i] == '	') && av[1][i + 1] != '\0')
				write (1, " ", 1);
		}
	}
	return (write(1, "\n", 1), 0);
}
